class Marks
{
	public static void main(String[] args)
	{
		float marks1=75.6f;
		float marks2=78.9f;
		float marks3=87.6f;
		float marks4=92.6f;
		float marks5=88.5f;
		float update_mark=5.0f;
		System.out.println("Original Marks");
		System.out.println(marks1);
		System.out.println(marks2);
		System.out.println(marks3);
		System.out.println(marks4);
		System.out.println(marks5);
		System.out.println("---------------------------");
		System.out.println("Updated Marks");
		System.out.println(marks1+update_mark);
		System.out.println(marks2+update_mark);
		System.out.println(marks3+update_mark);
		System.out.println(marks4+update_mark);
		System.out.println(marks5+update_mark);
	}
}